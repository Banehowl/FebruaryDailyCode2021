# Given an n-sided regular polygon n, return the total sum of the internal angles (in degrees)
# sum_polygon(3) -> 180
# sum_polygon(4) -> 360
# sum_polygon(6) -> 720

def sum_polygon(n):
        solution = (n-2) * 180
        return(solution)
